#include "../includes/libft.h"

char	*ft_strcpy(char *s, const char *src)
{
	size_t i;

	i = 0;
	while (src[i] != '\0')
	{
		s[i] = src[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
